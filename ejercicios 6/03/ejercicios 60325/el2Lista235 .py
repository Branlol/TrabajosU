Pasar = False
while Pasar != True:
    n = int(input("Escribe un numero:"))
    if n >= 0 :
        Pasar = True
    else:
        print("Ingrese un numero valido")
        
for i in range(2,n+1,1):
    if i % 2 == 0:
        print(i)
    elif i % 3 == 0:
        print(i)
    elif i % 5 == 0:
        print(i)
        
        
    
    

        
        
        
        
